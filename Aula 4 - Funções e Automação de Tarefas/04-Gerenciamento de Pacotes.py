# Instalar Pacotes: Comando 'pip install' nome_do_pacote para baixar e 
# instalar um pacote específico a partir do Python Package Index (PyPI), que é 
# o repositório de pacotes Python.

# Atualizar Pacotes: Comando 'pip install --upgrade' nome_do_pacote 
# para atualizar um pacote já instalado para a versão mais recente.

# Desinstalar Pacotes: Comando 'pip uninstall' nome_do_pacote para 
# remover um pacote que não é mais necessário.

# Listar Pacotes Instalados: Comando 'pip list' para listar todos os pacotes 
# instalados em seu ambiente Python

